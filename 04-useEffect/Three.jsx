import React, { useEffect, useState } from "react";

const Three = () => {
  const [image, setImage] = useState("");
  useEffect(() => {
    fetch("https://pokeapi.co/api/v2/pokemon/mankey")
      .then((resp) => {
        return resp.json();
      })
      .then((data) => {
        console.log({ data });
        setImage(data.sprites.back_default);
      });
  }, []);
  return (
    <div>
      Three
      <h1>Mankey</h1>
      <img src={image} alt="Mankey image" />
    </div>
  );
};

export default Three;
